void lcd_putch(BYTE c, BYTE fColor, BYTE bColor)
{
	BYTE col, row;
	if (c>'z' || c<' ') {
		return;
	}
	for(col=0; col<5; col++){
		for(row=0; row<8; row++){
			if( (lcd_font[c-' '][col]) & (0x80>>row) )
				lcd_send(fColor, LCD_TDATA);
			else
				lcd_send(bColor, LCD_TDATA);
		}
	}
	for(row=0; row<8; row++){
		lcd_send(bColor, LCD_TDATA);
	}
}

void lcd_text(const char *s, BYTE x, BYTE y, BYTE fColor, BYTE bColor)
{
	lcd_send(MADCTL, LCD_TCMD);   // pixel write direction
	// 7:MY  6:MX  5:V  4:LAO  3:RGB 2:x  1:x  0:x
	lcd_send(0b10100000, LCD_TDATA);

	lcd_send(CASET, LCD_TCMD);
	lcd_send(y, LCD_TDATA);
	lcd_send(y + 7, LCD_TDATA);
   
	lcd_send(PASET, LCD_TCMD);
	lcd_send(x, LCD_TDATA);
	lcd_send(131, LCD_TDATA);
   
	lcd_send(RAMWR, LCD_TCMD);

	while (*s) {
		lcd_putch(*s, fColor, bColor);
		++s;
	}
}

