void lcd_send(BYTE a, BYTE cmd)
{
	BYTE c;
	LCD_CE = 0;
	LCD_CLK = 0;
	//NOP();
	LCD_DATA = 1;
	if(cmd == LCD_TCMD)  LCD_DATA = 0;
	//else LCD_DATA = 1;
	NOP();
	LCD_CLK = 1;	
	for(c=8;c>0;c--)
	{
		LCD_CLK = 0;
		LCD_DATA = 0;
		if( a &0x80)	LCD_DATA = 1;
		NOP();
		LCD_CLK = 1;
		a = a<<1;
	}
	LCD_CE = 1;
}
void color_fill(BYTE x1, BYTE x2, BYTE y1, BYTE y2, BYTE color)
{
   unsigned long i, total;
   total = ( (unsigned long)x2-x1+1)*(y2-y1+1);
   lcd_send(CASET, LCD_TCMD);
   lcd_send(x1, LCD_TDATA);
   lcd_send(x2, LCD_TDATA);
   
   lcd_send(PASET, LCD_TCMD);
   lcd_send(y1, LCD_TDATA);
   lcd_send(y2, LCD_TDATA);
   
   lcd_send(RAMWR, LCD_TCMD);
   for(i=0; i<total; i++)   lcd_send(color, LCD_TDATA);

}
